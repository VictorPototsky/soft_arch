package lesson1;

public class Angle3D {
    private double angX,angY,angZ;

    public Angle3D(double angX, double angY, double angZ) {
        this.angX = angX;
        this.angY = angY;
        this.angZ = angZ;
    }

    public double getAngX() {
        return angX;
    }

    public void setAngX(double angX) {
        this.angX = angX;
    }

    public double getAngY() {
        return angY;
    }

    public void setAngY(double angY) {
        this.angY = angY;
    }

    public double getAngZ() {
        return angZ;
    }

    public void setAngZ(double angZ) {
        this.angZ = angZ;
    }
}
